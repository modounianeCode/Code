#include<stdio.h>
int main(){
//trier les elements du tableau dans l'ordre croissant
int T[]={2850,2950,3050,2880,2755,2710,2890,3130,2940,3325,2920,2880};
int length,i,j,tmp;
length=sizeof(T)/sizeof(T[0]);
for ( i = 0; i < length; i++)
{
   for ( j = i+1; j < length; j++)
   {
        if (T[i] > T[j])
        {
             tmp=T[i];
             T[i]=T[j];
             T[j]=tmp;   
        }       
   }  
}
puts("Tableau trie par ordre croissant");
for ( i = 0; i < length; i++)
{
        printf("%d ",T[i]);
}
puts("");
return 0;
}



































































































































































































































































































































